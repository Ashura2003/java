public class Math {
    
    public int addNumbers(int a, int b){
        int sum=a+b;
        return sum;
    }

    public int subNumbers(int a,int b){
        int diff;
        
        diff=a-b;
        
        return diff;
    }

    public int mulNumbers(int a , int b){
        int pro=a*b;
        return pro;
    }

    public int divNumbers(int a, int b){
        int div=a/b;
        return div;
    }

}
