public class loop{
	public static void main(String[] args) {
		int i=0;
		while(i<6){
			System.out.println(i);
			i++;
		}
		System.out.println("\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n\n");

		for (int a=0 ; a<6 ; a++){
			System.out.println(a);
		}

		do {
			System.out.println(i);
			i++;
		}while(i<6);
	}
}